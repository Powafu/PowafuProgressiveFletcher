package api.mule;

public class MuleInfo {

    private String rsn;
    private int world;

    public MuleInfo() {

    }

    public String getRsn() {
        return rsn;
    }

    public void setRsn(String rsn) {
        this.rsn = rsn;
    }

    public int getWorld() {
        return world;
    }

    public void setWorld(int world) {
        this.world = world;
    }
}

